        //gc.setTimeInMillis(jtxtFechaCorrida.getDate().getTime());
        XMLGregorianCalendar cal = null;
        try {
            cal = DatatypeFactory.newInstance().newXMLGregorianCalendar(gc);
        } catch (DatatypeConfigurationException ex) {
            Logger.getLogger(JFTMSVenta.class.getName()).log(Level.SEVERE, null, ex);
        }
        String[] array = jtxtFechaCorrida.getDateEditor().getTexto().split("/");
