package Datos;

/**
 *
 * @author brojasa
 */
  
 public class Puntos {
    String puntosOtorgados;
    String saldoPuntos;
    String saldoMoneda;
    
    /** Creates a new instance of Puntos */
    public Puntos() {
        puntosOtorgados = "0";
        saldoPuntos = "0";
        saldoMoneda = "0";
    }
    
    public void setPuntosOtorgados(String puntos){
        puntosOtorgados = puntos;
    }
    public String getPuntosOtorgados(){
        return puntosOtorgados;
    }
    
    public void setSaldoPuntos(String puntos){
        saldoPuntos = puntos;
    }
    public String getSaldoPuntos(){
        return saldoPuntos;
    }
    
    public void setSaldoMoneda(String puntos){
        saldoMoneda = puntos;
    }
    public String getSaldoMoneda(){
        return saldoMoneda;
    }
}

