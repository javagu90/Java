package Datos;

/**
 *
 * @author brojasa
 */
public class OperacionesResponse {
    
    Puntos saldos;
    StatusType status;
    /** Creates a new instance of OperacionesResponse */
    public OperacionesResponse() {
        saldos = new Puntos();       
        status = new StatusType();
    }
    
    public void setPuntos(Puntos saldos){
        this.saldos = saldos;        
    }
    public Puntos getPuntos(){
        return this.saldos;        
    }
    
    public void setStatus(StatusType status){
        this.status = status;        
    }
    public StatusType getStatus(){
        return this.status;        
    }
    
    
}
