    private void jtxt_folioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxt_folioKeyReleased
        //jtxt_folio
        if(evt.getKeyCode() == evt.VK_ESCAPE)
        {
            this.folioTar = "nada";
            this.dispose();
        }
        
        if(evt.getKeyCode() == evt.VK_ENTER){
            String fb = jtxt_folio.getText().toUpperCase();
            Vector vft = (Vector) busquedas.variosFacadeRemote.buscaTarjetaPorFolio(fb);
            if(vft.size()==0)
            {
                new jdlg_error("Â¡La tarjeta con el folio "+fb+" es invalida!","Verifique el folio de la tarjeta","Tarjeta invalida").setVisible(true);
                jtxt_folio.selectAll();
                return;
            }
            else
            {
                //Vector vf = (Vector)vft.get(0);
                this.folioTar = fb;
                this.vFolioTar = vft;
                this.dispose();
            }
        }
            
    }//GEN-LAST:event_jtxt_folioKeyReleased
    
