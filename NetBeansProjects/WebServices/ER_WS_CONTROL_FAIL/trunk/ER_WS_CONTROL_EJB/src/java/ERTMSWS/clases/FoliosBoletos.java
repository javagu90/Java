    private String SerieBoleto;
    private String Origen;
    private int NoAsiento;
    private String NombrePasajero;
    private BigDecimal ImporteCargo;
    private BigDecimal PuntosAcumSOC;
    private BigDecimal PuntosRedSOC;
    private BigDecimal SaltoPuntosSoc;
