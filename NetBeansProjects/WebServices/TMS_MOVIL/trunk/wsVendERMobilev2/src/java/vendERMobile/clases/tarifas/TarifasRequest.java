package vendERMobile.clases.tarifas;

/**
 *
 * @author asolis
 */
public class TarifasRequest {
    String uid;
    int idRuta;
    String cajaNombre;
    /** Creates a new instance of TarifasRequest */
    public TarifasRequest() {
    }
    
    public void setUid(String uid){
        this.uid = uid;
    }
    public String getUid(){
        return this.uid;
    }
    
    public void setIdRuta(int idRuta){
        this.idRuta = idRuta;
    }
    public int getIdRuta(){
        return this.idRuta;
    }
    
    public void setCajaNombre(String cajaNombre){
        this.cajaNombre = cajaNombre;
    }
    public String getCajaNombre(){
        return this.cajaNombre;
    }
}
