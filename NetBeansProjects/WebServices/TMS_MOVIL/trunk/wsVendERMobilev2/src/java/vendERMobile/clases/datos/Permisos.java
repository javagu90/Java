package vendERMobile.clases.datos;

import java.util.Vector;

/**
 *
 * @author asolis
 */
public class Permisos {
    int idPermiso;
    String permisoNombre;
    String permisoClave;
    String descripcion;
    String perfilNombre;
    
    /** Creates a new instance of Permisos */
    public Permisos() {
    }
    
    public Permisos(Vector permisos) {
        idPermiso = Integer.parseInt(permisos.get(0).toString());
        permisoNombre = permisos.get(1).toString();
        permisoClave = permisos.get(2).toString();
        descripcion = permisos.get(3).toString();
        perfilNombre = permisos.get(4).toString();
    }
    
    public void setIdPermiso(int idPermiso){
        this.idPermiso = idPermiso;
    }
    public int getIdPermiso(){
        return this.idPermiso;
    }
    
    public void setPermisoNombre(String permisoNombre){
        this.permisoNombre = permisoNombre;       
    }
    public String getPermisoNombre(){
        return this.permisoNombre;
    }
    
    public void setPermisoClave(String permisoClave){
        this.permisoClave = permisoClave;
    }
    public String getPermisoClave(){
        return this.permisoClave;
    }
        
    public void setDescripcion(String descripcion){
        this.descripcion = descripcion;
    }
    public String getDescripcion(){
        return this.descripcion;
    }    
            
    public void setPerfilNombre(String perfilNombre){
        this.perfilNombre = perfilNombre;
    }
    public String getPerfilNombre(){
        return this.perfilNombre;
    }
}
