package vendERMobile.clases.datos;

/**
 *
 * @author asolis
 */
public class AsientoAutobus {    
    private String numeroAsiento;
    private String tipoPasajero;
    private String nombreOcupante;
    private String folioPreImpresoBoleto;
    private String folioPreImpresoCancelacion;
    private String folioBoleto;
    private String folioCancelacion;
    private float importeBoleto;
    private String ciudadVenta;
    private String codigoBarras;
    
    /** Creates a new instance of AsientoAutobus */
    public AsientoAutobus() {
    }

    public String getNumeroAsiento() {
        return numeroAsiento;
    }

    public void setNumeroAsiento(String numeroAsiento) {
        this.numeroAsiento = numeroAsiento;
    }

    public String getTipoPasajero() {
        return tipoPasajero;
    }

    public void setTipoPasajero(String tipoPasajero) {        
        this.tipoPasajero = tipoPasajero;
    }

    public String getNombreOcupante() {
        return nombreOcupante;
    }

    public void setNombreOcupante(String nombreOcupante) {
        this.nombreOcupante = nombreOcupante;
    }

    public String getFolioPreImpresoBoleto() {
        return folioPreImpresoBoleto;
    }

    public void setFolioPreImpresoBoleto(String folioPreImpresoBoleto) {
        this.folioPreImpresoBoleto = folioPreImpresoBoleto;
    }

    public String getFolioPreImpresoCancelacion() {
        return folioPreImpresoCancelacion;
    }

    public void setFolioPreImpresoCancelacion(String folioPreImpresoCancelacion) {
        this.folioPreImpresoCancelacion = folioPreImpresoCancelacion;
    }

    public String getFolioBoleto() {
        return folioBoleto;
    }

    public void setFolioBoleto(String folioBoleto) {
        this.folioBoleto = folioBoleto;
    }

    public String getFolioCancelacion() {
        return folioCancelacion;
    }

    public void setFolioCancelacion(String folioCancelacion) {
        this.folioCancelacion = folioCancelacion;
    }

    public float getImporteBoleto() {
        return importeBoleto;
    }

    public void setImporteBoleto(float importeBoleto) {
        this.importeBoleto = importeBoleto;
    }

    public String getCiudadVenta() {
        return ciudadVenta;
    }

    public void setCiudadVenta(String ciudadVenta) {
        this.ciudadVenta = ciudadVenta;
    }

    /**
     * @return the codigoBarras
     */
    public String getCodigoBarras() {
        return codigoBarras;
    }

    /**
     * @param codigoBarras the codigoBarras to set
     */
    public void setCodigoBarras(String codigoBarras) {
        this.codigoBarras = codigoBarras;
    }
}
