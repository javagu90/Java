package vendERMobile.clases.venderBoletos;

import vendERMobile.clases.datos.AsientoAutobus;

/**
 *
 * @author asolis
 */
public class VenderBoletosResponse {
    private String uid;    
    private boolean success;
    private int errorCode;
    private String errorMsg;
    private String ciudadVenta;
    AsientoAutobus asientosSeleccionados[];
    
    /** Creates a new instance of VenderBoletosResponse */
    public VenderBoletosResponse() {
    }
    
    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public int getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(int errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorMsg() {
        return errorMsg;
    }

    public void setErrorMsg(String errorMsg) {
        this.errorMsg = errorMsg;
    }
    
    public String getCiudadVenta() {
        return ciudadVenta;
    }

    public void setCiudadVenta(String ciudadVenta) {
        this.ciudadVenta = ciudadVenta;
    }
    
    public AsientoAutobus[] getAsientoSeleccionados() {
        return asientosSeleccionados;
    }

    public void setAsientoSeleccionados(AsientoAutobus[] asientosSeleccionados) {
        this.asientosSeleccionados = asientosSeleccionados;
    }
}
