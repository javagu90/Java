package vendERMobile.clases.despachoTarjetas;

/**
 *
 * @author vgonzalez
 */
public class getdatosResponse {
  private String operador;
    private String autobus;
    private String empresa;
    
    public getdatosResponse(){

    }

    /**
     * @return the operador
     */
    public String getOperador() {
        return operador;
    }

    /**
     * @param operador the operador to set
     */
    public void setOperador(String operador) {
        this.operador = operador;
    }

    /**
     * @return the autobus
     */
    public String getAutobus() {
        return autobus;
    }

    /**
     * @param autobus the autobus to set
     */
    public void setAutobus(String autobus) {
        this.autobus = autobus;
    }

    /**
     * @return the empresa
     */
    public String getEmpresa() {
        return empresa;
    }

    /**
     * @param empresa the empresa to set
     */
    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }


}
