package vendERMobile.clases.permisos;

/**
 *
 * @author asolis
 */
public class PermisosRequest {
    String uid;
    String permisoClave;
    String usuarioNumero;
    String contrasenia;
    String cajaNombre;
    
    /**
     * Creates a new instance of PermisosRequest
     */
    public PermisosRequest() {
    }
    
    public String getUid() {
        return uid;
    }
    public void setUid(String uid) {
        this.uid = uid;
    }
    
    public String getNumUsuario() {
        return usuarioNumero;
    }
    public void setNumUsuario(String usuario) {
        this.usuarioNumero = usuario;
    }

    public String getContrasenia() {
        return contrasenia;
    }
    public void setContrasenia(String contrasenia) {
        this.contrasenia = contrasenia;
    }
    
    public String getpermisoClave() {
        return permisoClave;
    }
    public void setpermisoClave(String permisoClave) {
        this.permisoClave = permisoClave;
    }
    
    public String getcajaNombre() {
        return cajaNombre;
    }
    public void setcajaNombre(String cajaNombre) {
        this.cajaNombre = cajaNombre;
    }
}
