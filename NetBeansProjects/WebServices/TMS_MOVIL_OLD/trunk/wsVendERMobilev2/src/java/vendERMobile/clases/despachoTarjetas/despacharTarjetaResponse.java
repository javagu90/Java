package vendERMobile.clases.despachoTarjetas;

import java.util.List;

/**
 *
 * @author vgonzalez
 */ 
public class despacharTarjetaResponse {
    private int codigo = 1;
    private String mensaje = "!Error de datos!";
    private String folioTarjeta= "";;
    private String nombreOperador="";
    private List<Pasajero> ocupacion;
    private List<Folio> folios=null;

    public despacharTarjetaResponse(){

    }

    /**
     * @return the codigo
     */
    public int getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }
    /**
     * @return the mensaje
     */
    public String getMensaje() {
        return mensaje;
    }

    /**
     * @param mensaje the mensaje to set
     */
    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }

    /**
     * @return the folioTarjeta
     */
    public String getFolioTarjeta() {
        return folioTarjeta;
    }

    /**
     * @param folioTarjeta the folioTarjeta to set
     */
    public void setFolioTarjeta(String folioTarjeta) {
        this.folioTarjeta = folioTarjeta;
    }

    /**
     * @return the nombreOperador
     */
    public String getNombreOperador() {
        return nombreOperador;
    }

    /**
     * @param nombreOperador the nombreOperador to set
     */
    public void setNombreOperador(String nombreOperador) {
        this.nombreOperador = nombreOperador;
    }

    @Override
    public String toString() {
        return codigo+" = "+mensaje+" = "+folioTarjeta+" = "+nombreOperador;
    }





    /**
     * @return the ocupacion
     */
    public List<Pasajero> getOcupacion() {
        return ocupacion;
    }

    /**
     * @param ocupacion the ocupacion to set
     */
    public void setOcupacion(List<Pasajero> ocupacion) {
        this.ocupacion = ocupacion;
    }
    /**
     * @return the folios
     */
    public List<Folio> getFolios() {
        return folios;
    }

    /**
     * @param folios the folios to set
     */
    public void setFolios(List<Folio> folios) {
        this.folios = folios;
    }


}
