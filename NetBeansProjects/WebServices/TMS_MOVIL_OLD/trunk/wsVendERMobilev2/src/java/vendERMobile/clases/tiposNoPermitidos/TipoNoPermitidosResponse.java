package vendERMobile.clases.tiposNoPermitidos;

/**
 *
 * @author asolis
 */
public class TipoNoPermitidosResponse {
    
    String uid;    
    private boolean success = true;
    private int errorCode;
    private String errorMsg;
    String tiposNoPermitidos[];
    
    /** Creates a new instance of TipoNoPermitidosResponse */
    public TipoNoPermitidosResponse() {
    }    
            
    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public int getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(int errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorMsg() {
        return errorMsg;
    }

    public void setErrorMsg(String errorMsg) {
        this.errorMsg = errorMsg;
    } 
    
    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid; 
    } 

    public void setTiposNoPermitidos(String[] tiposNoPermitidos){
        this.tiposNoPermitidos = tiposNoPermitidos;
    }
    public String[] getTiposNoPermitidos(){
        return this.tiposNoPermitidos;
    }
    
}
