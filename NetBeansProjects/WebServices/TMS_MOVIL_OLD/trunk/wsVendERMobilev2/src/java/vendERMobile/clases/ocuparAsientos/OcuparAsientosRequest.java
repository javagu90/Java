package vendERMobile.clases.ocuparAsientos;

import vendERMobile.clases.datos.Corridas;
import vendERMobile.clases.datos.Sesion;

/**
 *
 * @author asolis
 */
public class OcuparAsientosRequest {
    String uid;
    Sesion sesion;
    Corridas corrida;
    String mapaAsientosOcupados;
    String mapaAsientosPasajero;
    String modoOcupacion;    
    /** Creates a new instance of OcuparAsientosRequest */
    public OcuparAsientosRequest() {
    }
    
    public void setUid(String uid){
        this.uid = uid;
    }
    public String getUid(){
        return this.uid;
    }
    
    public void setSesion(Sesion sesion){
        this.sesion = sesion;
    }
    public Sesion getSesion(){
        return this.sesion;
    }
    
    public void setCorridas(Corridas corridas){
        this.corrida = corridas;
    }
    public Corridas getCorridas(){
        return this.corrida;
    }
    
    public void setMapaAsientosOcupados(String mapaAsientosOcupados){
        this.mapaAsientosOcupados = mapaAsientosOcupados;
    }
    public String getMapaAsientosOcupados(){
        return this.mapaAsientosOcupados;
    }
    
    public void setMapaAsientosPasajero(String mapaAsientosPasajero){
        this.mapaAsientosPasajero = mapaAsientosPasajero;
    }
    public String getMapaAsientosPasajero(){
        return this.mapaAsientosPasajero;
    }
    
    public void setModoOcupacion(String modoOcupacion){
        this.modoOcupacion = modoOcupacion;
    }
    public String getModoOcupacion(){
        return this.modoOcupacion;
    }
    
}
