package vendERMobile.clases.tiposPago;

/**
 *
 * @author asolis
 */
public class TipoPagoRequest {
    String uid;
    String cajaNombre;
    /**
     * Creates a new instance of TipoPagoRequest
     */
    public TipoPagoRequest() {
    }        
    
    public void setUid(String uid){
        this.uid = uid;
    }
    public String getUid(){
        return this.uid;
    }
    
    public void setCajaNombre(String cajaNombre){
        this.cajaNombre = cajaNombre;
    }
    public String getCajaNombre(){
        return this.cajaNombre;
    }
    
}
