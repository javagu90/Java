public class ItinerariosPorRutaRequest {
  
    private String uid;    
    private String rutaId;    
    String cajaNombre;
    /** Creates a new instance of ItinerariosPorRutaRequest */
    
    public ItinerariosPorRutaRequest() {
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getRutaId() {
        return rutaId;
    }

    public void setRutaId(String rutaId) {
        this.rutaId = rutaId;
    }
    
    public String getcajaNombre() {
        return cajaNombre;
    }
    public void setcajaNombre(String cajaNombre) {
        this.cajaNombre = cajaNombre;
    }
    
}
