package vendERMobile.clases.despachoTarjetas;

/**
 *
 * @author vgonzalez
 */
public class Folio {
private int asientoNumero;
    private String folioPreimpreso;

    public Folio() {

    }

    /**
     * @return the asientoNumero
     */
    public int getAsientoNumero() {
        return asientoNumero;
    }

    /**
     * @param asientoNumero the asientoNumero to set
     */
    public void setAsientoNumero(int asientoNumero) {
        this.asientoNumero = asientoNumero;
    }

    /**
     * @return the folioPreimpreso
     */
    public String getFolioPreimpreso() {
        return folioPreimpreso;
    }

    /**
     * @param folioPreimpreso the folioPreimpreso to set
     */
    public void setFolioPreimpreso(String folioPreimpreso) {
        this.folioPreimpreso = folioPreimpreso;
    }
}

