package vendERMobile.clases.tiposPago;

import vendERMobile.clases.datos.TiposPago;

/**
 *
 * @author asolis
 */
public class TipoPagoResponse {
    String uid;    
    private boolean success = true;
    private int errorCode;
    private String errorMsg;
    TiposPago[] tiposPago;
    /** Creates a new instance of TipoPagoResponse */
    public TipoPagoResponse() {
    }
            
    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public int getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(int errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorMsg() {
        return errorMsg;
    }

    public void setErrorMsg(String errorMsg) {
        this.errorMsg = errorMsg;
    } 
    
    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid; 
    } 
    
    public void setTiposPago(TiposPago[] tiposPago){
        this.tiposPago = tiposPago;
    }
    public TiposPago[] getTiposPago(){
        return this.tiposPago;
    }
    
}
