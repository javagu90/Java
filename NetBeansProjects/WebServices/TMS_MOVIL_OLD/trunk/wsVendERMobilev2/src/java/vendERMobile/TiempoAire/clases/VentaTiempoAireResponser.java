package vendERMobile.TiempoAire.clases;

/**
 *
 * @author brojasa
 */
public class VentaTiempoAireResponser {  
    String status ="";
    String mensaje ="";
    
    
    /** Creates a new instance of VentaTiempoAireResponser */
    public VentaTiempoAireResponser() {
        
    }
    
    public VentaTiempoAireResponser(String status , String mensaje ) {
      this.status =status;
      this.mensaje =mensaje;
    }
    
   public String getStatus()
   {
    return this.status;
   }
    
   
   public void setStatus(String status)
   {
     this.status = status;
   }
    
  public String getMensaje()
   {
    return this.mensaje;
   }  
    
  
  public void setMensaje(String mensaje)
   {
    this.mensaje = mensaje;
   }
}
