package wsc.entidades;

import java.util.Vector;

/**
 *
 * @author vgonzalez
 */
public class compra {
    private String fechaCorrida;
    private String origen;
    private String destino;
    private String servcio;
    private String empresa;
    private long noAsiento;
    private String tipoPasajero;
    private String nombrePasajero;
    private double importeBoleto;
    private String folio;
    private String operacion;
    private String fechaOperacion;
    
    /** Creates a new instance of reservacion */
    public compra() {
    }
    
    public compra(Vector comp) {
        this.setFechaCorrida(comp.get(0).toString());
        this.setOrigen(comp.get(1).toString());
        this.setDestino(comp.get(2).toString());
        this.setServcio(comp.get(3).toString());
        this.setEmpresa(comp.get(4).toString());
        this.setNoAsiento(Long.valueOf(comp.get(5).toString()));
        this.setnombrePasajero(comp.get(6).toString());
        this.setTipoPasajero(comp.get(7).toString());
        this.setimporteBoleto(Double.valueOf(comp.get(8).toString()));
        this.setfolio(comp.get(9).toString());
        this.setoperacion(comp.get(10).toString());
        this.setfechaOperacion(comp.get(11).toString());
    }
    
    public String getFechaCorrida() {
        return fechaCorrida;
    }

    public void setFechaCorrida(String fechaCorrida) {
        this.fechaCorrida = fechaCorrida;
    }

    public String getOrigen() {
        return origen;
    }

    public void setOrigen(String origen) {
        this.origen = origen;
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

    public String getServcio() {
        return servcio;
    }

    public void setServcio(String servcio) {
        this.servcio = servcio;
    }

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public long getNoAsiento() {
        return noAsiento;
    }

    public void setNoAsiento(long noAsiento) {
        this.noAsiento = noAsiento;
    }

    public String getTipoPasajero() {
        return tipoPasajero;
    }

    public void setTipoPasajero(String tipoPasajero) {
        this.tipoPasajero = tipoPasajero;
    }

    public String getnombrePasajero() {
        return nombrePasajero;
    }

    public void setnombrePasajero(String nombrePasajero) {
        this.nombrePasajero = nombrePasajero;
    }

    public double getimporteBoleto() {
        return importeBoleto;
    }

    public void setimporteBoleto(double importeBoleto) {
        this.importeBoleto = importeBoleto;
    }

    public String getfolio() {
        return folio;
    }

    public void setfolio(String folio) {
        this.folio = folio;
    }

    public String getoperacion() {
        return operacion;
    }

    public void setoperacion(String operacion) {
        this.operacion = operacion;
    }

    public String getfechaOperacion() {
        return fechaOperacion;
    }

    public void setfechaOperacion(String fechaOperacion) {
        this.fechaOperacion = fechaOperacion;
    }


}
