package tmspcweb.clases;

import tmspcweb.clases.datos.ReservacionResp;
/**
 *
 * @author ocruz
 */
public class reservacionResponse {
    
    private String uid;
    private ReservacionResp reservacionIda;
    private ReservacionResp reservacionRegreso;
    private double importeTotal;
    private boolean success;
    private int errorCode;
    private String errorMsg;
    
    /** Creates a new instance of reservacionResponse */
    public reservacionResponse() {
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public ReservacionResp getReservacionIda() {
        return reservacionIda;
    }

    public void setReservacionIda(ReservacionResp reservacionIda) {
        this.reservacionIda = reservacionIda;
    }

    public ReservacionResp getReservacionRegreso() {
        return reservacionRegreso;
    }

    public void setReservacionRegreso(ReservacionResp reservacionRegreso) {
        this.reservacionRegreso = reservacionRegreso;
    }

    public double getImporteTotal() {
        return importeTotal;
    }

    public void setImporteTotal(double importeTotal) {
        this.importeTotal = importeTotal;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public int getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(int errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorMsg() {
        return errorMsg;
    }

    public void setErrorMsg(String errorMsg) {
        this.errorMsg = errorMsg;
    }    
}
