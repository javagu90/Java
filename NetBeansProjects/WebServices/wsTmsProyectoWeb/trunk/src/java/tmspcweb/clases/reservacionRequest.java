package tmspcweb.clases;

import tmspcweb.clases.datos.ReservacionReq;
import tmspcweb.clases.datos.Sesion;

/**
 *
 * @author ocruz
 */
public class reservacionRequest {
    
    private String uid;
    private Sesion sesion;
    private String banCompra; //decide si es una reservacion de compra o no. 1 es de compra y 0 es de reservacion
    private ReservacionReq datosIda;
    private ReservacionReq datosRegreso;
    
    /** Creates a new instance of reservacionRequest */
    public reservacionRequest() {
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public Sesion getSesion() {
        return sesion;
    }

    public void setSesion(Sesion sesion) {
        this.sesion = sesion;
    }

    public ReservacionReq getDatosIda() {
        return datosIda;
    }

    public void setDatosIda(ReservacionReq datosIda) {
        this.datosIda = datosIda;
    }

    public ReservacionReq getDatosRegreso() {
        return datosRegreso;
    }

    public void setDatosRegreso(ReservacionReq datosRegreso) {
        this.datosRegreso = datosRegreso;
    }    
    
    public void setbanCompra(String banCompra) {
        this.banCompra = banCompra;
    }
    
    public String getbanCompra(){
        return banCompra;
    }
}
