package ERTMSWS.clases;

import java.io.Serializable;

/**
 *
 * @author vgonzalez
 */
public class Pagos implements Serializable {
    private String TipoPago;
    private float ImportePago;
    private String NumOperacion;
    private String NumReferencia ;
    private String NumAutorizacion;
    private String NumTarjeta;
    private String NombreBanco;

    public Pagos(){

    }

    /**
     * @return the TipoPago
     */
    public String getTipoPago() {
        return TipoPago;
    }

    /**
     * @param TipoPago the TipoPago to set
     */
    public void setTipoPago(String TipoPago) {
        this.TipoPago = TipoPago;
    }

    /**
     * @return the ImportePago
     */
    public float getImportePago() {
        return ImportePago;
    }

    /**
     * @param ImportePago the ImportePago to set
     */
    public void setImportePago(float ImportePago) {
        this.ImportePago = ImportePago;
    }

    /**
     * @return the NumOperacion
     */
    public String getNumOperacion() {
        return NumOperacion;
    }

    /**
     * @param NumOperacion the NumOperacion to set
     */
    public void setNumOperacion(String NumOperacion) {
        this.NumOperacion = NumOperacion;
    }

    /**
     * @return the NumReferencia
     */
    public String getNumReferencia() {
        return NumReferencia;
    }

    /**
     * @param NumReferencia the NumReferencia to set
     */
    public void setNumReferencia(String NumReferencia) {
        this.NumReferencia = NumReferencia;
    }

    /**
     * @return the NumAutorizacion
     */
    public String getNumAutorizacion() {
        return NumAutorizacion;
    }

    /**
     * @param NumAutorizacion the NumAutorizacion to set
     */
    public void setNumAutorizacion(String NumAutorizacion) {
        this.NumAutorizacion = NumAutorizacion;
    }

    /**
     * @return the NumTarjeta
     */
    public String getNumTarjeta() {
        return NumTarjeta;
    }

    /**
     * @param NumTarjeta the NumTarjeta to set
     */
    public void setNumTarjeta(String NumTarjeta) {
        this.NumTarjeta = NumTarjeta;
    }

    /**
     * @return the NombreBanco
     */
    public String getNombreBanco() {
        return NombreBanco;
    }

    /**
     * @param NombreBanco the NombreBanco to set
     */
    public void setNombreBanco(String NombreBanco) {
        this.NombreBanco = NombreBanco;
    }
}
