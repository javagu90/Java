package WS_CONTROL.TMS.entidades;

import java.io.Serializable;
import java.util.List;

/**
 *
 * @author brojas
 */
public class RutaItinerarios implements Serializable{
private String	  Empresa;
    private String Servicio;
    private String NombreServicio;
    private List<RutaItinerario> Ruta;

    /**
     * @return the Empresa
     */
    public String getEmpresa() {
        return Empresa;
    }

    /**
     * @param Empresa the Empresa to set
     */
    public void setEmpresa(String Empresa) {
        this.Empresa = Empresa;
    }

    /**
     * @return the RutaItinerarios
     */
    public String getServicio() {
        return Servicio;
    }

    /**
     * @param RutaItinerarios the RutaItinerarios to set
     */
    public void setServicio(String Servicio) {
        this.Servicio = Servicio;
    }

    /**
     * @return the ClaveServicio
     */
    public String getNombreServicio() {
        return NombreServicio;
    }

    /**
     * @param ClaveServicio the ClaveServicio to set
     */
    public void setNombreServicio(String ClaveServicio) {
        this.NombreServicio = ClaveServicio;
    }

    /**
     * @return the RutaItinerario
     */
    public List<RutaItinerario> getRuta() {
        return Ruta;
    }

    /**
     * @param RutaItinerario the RutaItinerario to set
     */
    public void setRuta(List<RutaItinerario> Ruta) {
        this.Ruta = Ruta;
    }




}
