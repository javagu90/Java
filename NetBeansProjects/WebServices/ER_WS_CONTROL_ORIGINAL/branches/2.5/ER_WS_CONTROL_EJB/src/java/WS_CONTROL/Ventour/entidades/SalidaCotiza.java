package WS_CONTROL.Ventour.entidades;

import java.io.Serializable;
import java.util.List;

/**
 *
 * @author brojas
 */
public class SalidaCotiza implements Serializable{
    private List<ProductoCotiza>  productos = null;
    private List<Unidad> lista_unidades = null;

   

    /**
     * @return the lista_unidades
     */
    public List<Unidad> getLista_unidades() {
        return lista_unidades;
    }

    /**
     * @param lista_unidades the lista_unidades to set
     */
    public void setLista_unidades(List<Unidad> lista_unidades) {
        this.lista_unidades = lista_unidades;
    }

    /**
     * @return the productos
     */
    public List<ProductoCotiza> getProductos() {
        return productos;
    }

    /**
     * @param productos the productos to set
     */
    public void setProductos(List<ProductoCotiza> productos) {
        this.productos = productos;
    }

}
