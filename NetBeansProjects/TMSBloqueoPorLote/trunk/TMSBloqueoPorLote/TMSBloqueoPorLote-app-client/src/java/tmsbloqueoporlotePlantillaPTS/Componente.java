package tmsbloqueoporlotePlantillaPTS;

public class Componente {
    private int X = 0;
    private int Z = 0;
    private String nombre;
    private int Ubicacion;
    private int Tipo;
    private int iId;
    private boolean bVisible = true;

    public Componente() {
    }

    public void setXZ(int pX, int pZ) {
        this.X = pX;
        this.Z = pZ;
    }

    public void setX(int pX) {
        this.X = pX;
    }

    public void setZ(int pZ) {
        this.Z = pZ;
    }

    public int getX() {
        return this.X;
    }

    public int getZ() {
        return this.Z;
    }

    public void setNombre(String pNombre) {
        this.nombre = pNombre;
    }

    public void setUbicacion(int pNo) {
        this.Ubicacion = pNo;
    }

    public void setTipo(int pNo) {
        this.Tipo = pNo;
    }

    public int getUbicacion() {
        return this.Ubicacion;
    }

    public int getTipo() {
        return this.Tipo;
    }

    public String getNombre() {
        return this.nombre;
    }

    public void setId(int pId) {
        this.iId = pId;
    }

    public int getId() {
        return this.iId;
    }

    public void setVisible(boolean pVisible) {
        this.bVisible = pVisible;
    }

    public boolean getVisible() {
        return this.bVisible;
    }
}
