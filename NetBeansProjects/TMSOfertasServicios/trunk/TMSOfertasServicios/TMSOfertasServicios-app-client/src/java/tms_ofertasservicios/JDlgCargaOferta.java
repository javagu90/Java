    private void jCboOfsKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jCboOfsKeyPressed
// TODO add your handling code here:
        switch(evt.getKeyCode()){
            case KeyEvent.VK_ESCAPE: this.dispose(); break;
            case KeyEvent.VK_ENTER: this.ofs = jCboOfs.getSelectedItem().toString();
                jDlgSiNo = new JDlgSiNo("Confirme.", "<html>¿Realmente desea cargar la oferta<br><font color=ff0000>"+this.ofs+"</font>?</html>");
                if(!jDlgSiNo.getResultado()) return;
                this.dispose();
                break;
        }
    }//GEN-LAST:event_jCboOfsKeyPressed
