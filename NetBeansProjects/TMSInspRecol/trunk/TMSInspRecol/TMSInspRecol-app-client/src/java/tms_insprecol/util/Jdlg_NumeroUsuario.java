    public void keyPressed(KeyEvent e) {
        switch(e.getKeyCode()){
            case KeyEvent.VK_F:
                    if(e.isAltDown()){
                        JTxtNumero.setText("");
                        JTxtNumero.requestFocusInWindow();
                    }
                break;
            case KeyEvent.VK_ESCAPE: 
                    numeroUsuario="";
                    this.dispose();
                break;
            case KeyEvent.VK_ENTER:
                if(e.getComponent().getName().equals("txtNumero")){
                    numeroUsuario = JTxtNumero.getText();
                    if(numeroUsuario.equals("")) return;
                    JBtnBuscar.doClick();
                }
                if(e.getComponent().getName().equals("btnboton")) JBtnBuscar.doClick();
                break;
        }
    }

