    private String getPFecha(String aux){
        String xfecha=aux;
        if(xfecha.equals("") || (xfecha.length()==1 || xfecha.length()>3 &&  xfecha.length()<10)){
            JOptionPane.showMessageDialog(this, "Escriba una fecha válida.", "¡Aviso!", JOptionPane.ERROR_MESSAGE);
            JTxtNumero.setText("");
            JTxtNumero.requestFocusInWindow();
            return "";
        }
        
        if(xfecha.length()==2 || xfecha.length()==3){
            String dtStrFecha = formatoFecha.format(new Date()).substring(3);
            if(xfecha.length()==2) xfecha = xfecha +"/"+dtStrFecha;
            else xfecha = xfecha+dtStrFecha;
            return xfecha;
        }
        return aux;
    }
