    private void jTblAutobusesKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTblAutobusesKeyPressed
// TODO add your handling code here:
        if(jTblAutobuses.getSelectedRow()==-1) return;
        switch(evt.getKeyCode()){
            case KeyEvent.VK_F8: this.dispose(); break;
            case KeyEvent.VK_ESCAPE: this.Fila=-1; this.dispose(); break;
            case KeyEvent.VK_ENTER:
                xListado.setDataVector(null, encabezado);
                AnchoColumnas();
                this.Fila=-1;
                jLblBarraEstado.setText(barraMensajes.getMsgAcceso(1));
                jTxtEco.setText("%");
                jTxtEco.selectAll();
                jTxtEco.requestFocusInWindow();
                break;
        }
    }//GEN-LAST:event_jTblAutobusesKeyPressed
    
