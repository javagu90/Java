    private void jTblAutobusesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTblAutobusesKeyReleased
// TODO add your handling code here:
        switch(evt.getKeyCode()){
            case KeyEvent.VK_UP: case KeyEvent.VK_DOWN: 
                case KeyEvent.VK_PAGE_UP: case KeyEvent.VK_PAGE_DOWN: 
                    this.Fila=jTblAutobuses.getSelectedRow();
                break;
        }
    }//GEN-LAST:event_jTblAutobusesKeyReleased

    private void jTxtEcoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtEcoKeyPressed
// TODO add your handling code here:
        switch(evt.getKeyCode()){
            case KeyEvent.VK_ESCAPE: this.Fila=-1; this.dispose(); break;
            case KeyEvent.VK_ENTER: 
                if(jTxtEco.getText().equals("")){
                    DialogoAceptar = new JDlgAceptar("¡Ingrese un criterio de busqueda!", "Si desea cancelar presione ESC.", Color.RED);
                    return;
                }
                datos = sesionVenta.busquedaAutobus(jTxtEco.getText());
                if(datos==null){
                    DialogoAceptar = new JDlgAceptar("¡La consulta no genero resultados!", "Intentelo con otro criterio.", Color.RED);
                    jTxtEco.selectAll();
                    jTxtEco.requestFocusInWindow();
                    return;
                }
                xListado.setDataVector(datos, encabezado);
                AnchoColumnas();
                jLblBarraEstado.setText(barraMensajes.getMsgAcceso(2));
                this.Fila=0;
                jTblAutobuses.setColumnSelectionInterval(0,0);
                jTblAutobuses.setRowSelectionInterval(0,0);
                jTblAutobuses.requestFocusInWindow();
                break;
        }
    }//GEN-LAST:event_jTxtEcoKeyPressed

