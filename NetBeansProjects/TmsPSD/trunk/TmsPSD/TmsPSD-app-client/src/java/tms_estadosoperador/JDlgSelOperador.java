    private void jTblOperadoresKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTblOperadoresKeyReleased
// TODO add your handling code here:
        switch(evt.getKeyCode()){
            case KeyEvent.VK_UP: case KeyEvent.VK_DOWN: 
                case KeyEvent.VK_PAGE_UP: case KeyEvent.VK_PAGE_DOWN: 
                    this.Fila=jTblOperadores.getSelectedRow();
                break;
        }
    }//GEN-LAST:event_jTblOperadoresKeyReleased

    private void jTxtClaveOperadorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtClaveOperadorKeyPressed
// TODO add your handling code here:
        switch(evt.getKeyCode()){
            case KeyEvent.VK_ESCAPE: this.Fila=-1; this.dispose(); break;
            case KeyEvent.VK_ENTER: 
                if(jTxtClaveOperador.getText().equals("")){
                    DialogoAceptar = new JDlgAceptar("¡Ingrese un criterio de busqueda!", "Si desea cancelar presione ESC.", Color.RED);
                    return;
                }
                datos = sesionVenta.listaOperadores(jTxtClaveOperador.getText());
                if(datos==null){
                    DialogoAceptar = new JDlgAceptar("¡La consulta no genero resultados!", "Intentelo con otro criterio.", Color.RED);
                    jTxtClaveOperador.selectAll();
                    jTxtClaveOperador.requestFocusInWindow();
                    return;
                }
                xListado.setDataVector(datos, encabezado);
                AnchoColumnas();
                jLblBarraEstado.setText(barraMensajes.getMsgEstadosOperador(2));
                this.Fila=0;
                jTblOperadores.setColumnSelectionInterval(0,0);
                jTblOperadores.setRowSelectionInterval(0,0);
                jTblOperadores.requestFocusInWindow();
                break;
        }
    }//GEN-LAST:event_jTxtClaveOperadorKeyPressed

