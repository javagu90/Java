    private void jTblOperadoresKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTblOperadoresKeyPressed
// TODO add your handling code here:
        if(jTblOperadores.getSelectedRow()==-1) return;
        switch(evt.getKeyCode()){
            case KeyEvent.VK_F8: this.dispose(); break;
            case KeyEvent.VK_ESCAPE: this.Fila=-1; this.dispose(); break;
            case KeyEvent.VK_ENTER:
                xListado.setDataVector(null, encabezado);
                AnchoColumnas();
                this.Fila=-1;
                jLblBarraEstado.setText(barraMensajes.getMsgEstadosOperador(1));
                jTxtClaveOperador.setText("%");
                jTxtClaveOperador.selectAll();
                jTxtClaveOperador.requestFocusInWindow();
                break;
        }
    }//GEN-LAST:event_jTblOperadoresKeyPressed
    
