    private void AnchoColumnas(){
        TableColumn column = null;
        int anchoContenedor = jScpOperadores.getWidth();
        for (int i = 0; i < 2; i++) {
            column = jTblOperadores.getColumnModel().getColumn(i);
            // "Clave", "Nombre"
            switch(i){
                case 0:  column.setPreferredWidth(Math.round(anchoContenedor*(float)0.25)); break;
                case 1:  column.setPreferredWidth(Math.round(anchoContenedor*(float)0.75)); break;
            }
            column.setResizable(false);
        }
    }
    
    private void centrarJDialogo(){
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension frameSize = this.getSize();
        if (frameSize.height > screenSize.height) {
            frameSize.height = screenSize.height;
        }
        if (frameSize.width > screenSize.width) {
            frameSize.width = screenSize.width;
        }
        this.setLocation( ( screenSize.width - frameSize.width ) / 2, ( screenSize.height - frameSize.height ) / 2 );
    }
    
