    /**
     * Guarda los datos que se obtuvieron de la consulta en el archivo especificado
     * @param nombre Es el archivo en donde se va a almacenar toda la información
     * @throws java.io.FileNotFoundException En caso de encontrar el archivo especifiacdo o no poder almacenar los datos sobre el mismo, esta excepción es lanzada
     */
    public void Guardar(File nombre) throws FileNotFoundException {
        String ext = "";
        if(jFileChooser.getFileFilter().getDescription().equals("MS Excel(*.xls)")){
            ext = ".xls";
        }else if (jFileChooser.getFileFilter().getDescription().equals("Txt(*.txt)")){
            ext = ".txt";
        }
        FileOutputStream guardo = new FileOutputStream(nombre+ext);
        PrintStream ps = new PrintStream(guardo);
        ps.print(salida);
        ps.flush();
        ps.close();
        Abrir(nombre, ext);
    }
    
