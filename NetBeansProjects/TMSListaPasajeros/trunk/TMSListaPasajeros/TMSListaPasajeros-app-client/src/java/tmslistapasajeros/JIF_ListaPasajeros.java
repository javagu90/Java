    /**
     * Atrapa todos los eventos del teclado y los procesa dependiendo de la tecla presionada
     * @param e e es de tipo KeyEvent y atrapa las propiedades disponibles de la tecla presionada
     */
    public void Evento_Tecla(KeyEvent e) {
        try {
            
            if(e.getKeyCode() == e.VK_1 && e.isControlDown()){
                setEventoTeclado(e);
                try {this.setIcon(true);} catch (PropertyVetoException ex) { ; }
                return;
            }

            if(e.getKeyCode() == e.VK_2 && e.isControlDown()){
                try {this.setIcon(true);} catch (PropertyVetoException ex) { ; }
                return;
            }

            if(e.getKeyCode() == e.VK_F4) {
            //System.out.println("Adiiiooos =)");
                int result = JOptionPane.showOptionDialog(this, "¿Desea cerrar Lista de Pasajeros?",
                            "TMS-Lista de Pasajeros", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
                if(result == JOptionPane.YES_OPTION)
                    this.dispose();
            }else if (e.getKeyCode() == e.VK_F9) {
                //Guardar Cosas!!!
            int result = jFileChooser.showSaveDialog(this);
            if (result != JFileChooser.CANCEL_OPTION) {
                File nombre = jFileChooser.getSelectedFile();
                if ( ( nombre == null ) || ( nombre.getName().equals( "" ) ) ) {
                    JOptionPane.showMessageDialog( this, "Nombre de Archivo Invalido",
                            "TMS - Lista de Pasajeros", JOptionPane.ERROR_MESSAGE );
                } else  if(nombre.exists()) {
                        JOptionPane.showMessageDialog( this, "Archivo Existente",
                            "TMS - Lista de Pasajeros", JOptionPane.ERROR_MESSAGE );
                    } else
                     Guardar(nombre);
                }
            }else if (e.getKeyCode() == e.VK_F3) {
                registros = 0;
                String fec, hor, nom, ori;

                fec = jdch_fecha.dateEditor.getTexto();
                hor = jhtxt_hora.getText();

                ori =(String) jcmb_origen.getSelectedItem();
                nom = jtxt_nombre.getText();
                
                salida = "Pasajero\tNumero Asiento\tForma Pago\tTipo Pasajero\tFecha-Hora Venta\tClave Corrida\tFecha-Hora Corrida\tDestino\tCaja\tTipo Operacion\t\n";
                if (ori.equals("")||fec.equals("")|| nom.equals("")) {
                    JOptionPane.showMessageDialog( this, "Origen, Fecha y Nombre son campos obligatorios. \nPor favor, llene estos campos e intente de nuevo","TMS-Lista de Pasajeros", 
                            JOptionPane.ERROR_MESSAGE );   
                    datos = null;
                    registros = 0;
                    numeroRegistros();
                }else if((nom.length() != 0)&&((nom.length() < 3)||(nom.length() > 50))) {
                            JOptionPane.showMessageDialog( this, "La longitud del nombre debe ser mayor de 3 pero menor de 50\nIntente de nuevo, por favor","TMS-Lista de Pasajeros", 
                                JOptionPane.ERROR_MESSAGE );
                        datos = null;
                        registros = 0;
                        numeroRegistros();
                    } else {
                        Vector lineas = null;
                        jProgressBar1.setIndeterminate(true);
                        System.out.println(ori + " " +fec+ " " + hor+ " "+ nom);
                        lineas = cosa.consultaPasajeros(ori,fec,hor,nom);
                        System.out.println("Lineas "+lineas);
                        
                        if((lineas!= null) && (lineas.size() >0)) {
                            Vector ll = (Vector) lineas.get(0);
                            if(!(ll.get(0).toString().equals("No puedo"))) {
                                int limite = Math.min(num,lineas.size());
                                datos = new String[limite][10];
                                jProgressBar1.setMaximum(limite);
                                int cont = 0;
                                Vector campos = null;
                                for (cont = 0; cont < limite; cont++) {
                                    campos = (Vector) lineas.get(cont);
                                    registros ++;
                                   for(int j = 0; j < campos.size(); j++) {
                                        if(campos.get(j) == null) {
                                            datos[cont][j] = "";
                                            salida = "" + salida + "\t";
                                        }else {
                                            datos[cont][j] = campos.get(j).toString();
                                            salida = "" + salida + datos[cont][j];
                                        }
                                        salida = "" + salida + "\t";
                                    }
                                    salida = "" +salida + "\n";
                                }
                            }else {
                                JOptionPane.showMessageDialog(this, "La búsqueda contiene demasiados registros\nPor favor, limite la busqueda e intente de nuevo ", "TMS-Lista de Pasajeros", JOptionPane.WARNING_MESSAGE);
                                datos = null;
                                registros = 0;
                                numeroRegistros();
                            }
                                
                        }else{
                            JOptionPane.showMessageDialog(this, "No existe registro alguno. \nPor favor, intente de nuevo", "TMS-Lista de Pasajeros", JOptionPane.WARNING_MESSAGE);
                            datos = null;
                            registros = 0;
                            numeroRegistros();
                        }
                    }
                
                jtxt_numero.setText(String.valueOf(registros));
                modelo.setDataVector(datos, encabezados);
                resizeColumnasCorridas();
                jProgressBar1.setIndeterminate(false);
            }
        }catch(Exception ex){
            System.out.println(ex);
        }
        
    }

