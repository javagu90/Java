    private void jtxt_nombreClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxt_nombreClienteKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_ENTER) 
        {
            if(jtxt_nombreCliente.getText().length()<4)
            {
                this.setAlwaysOnTop(false);
                this.setModal(false);
                JOptionPane.showMessageDialog(this,"Debes escribir un nombre.","TMS Venta.",JOptionPane.ERROR_MESSAGE);
                this.setAlwaysOnTop(true);
                this.setModal(true);
                jtxt_nombreCliente.selectAll();
                jtxt_nombreCliente.requestFocus();
            }
            else
            {
                this.setNombre(jtxt_nombreCliente.getText());
                dispose();
            }
        }
        if(evt.getKeyCode()==KeyEvent.VK_ESCAPE) 
        {
            this.setNombre("");
            dispose();
        }
        
        if(evt.getKeyCode()==KeyEvent.VK_UP || evt.getKeyCode()==KeyEvent.VK_DOWN)
            jBtnAceptar.requestFocus();
        
    }//GEN-LAST:event_jtxt_nombreClienteKeyPressed

    
