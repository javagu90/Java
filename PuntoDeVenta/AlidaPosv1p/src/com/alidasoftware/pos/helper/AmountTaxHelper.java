package com.alidasoftware.pos.helper;

import java.io.Serializable;

public class AmountTaxHelper implements Serializable {

	private static final long serialVersionUID = 5183013195432114276L;
	private float amount;
	private float tax;
	private float discount;

	public AmountTaxHelper() {
		
	}
	
	public AmountTaxHelper(float price, float tax, float discount) {
		this.amount   = price;
		this.tax      = tax; 
		this.discount = discount;
	}
	public float getAmount() {
		return amount;
	}
	
	public void setAmount(float amount) {
		this.amount = amount;
	}
	
	public float getTax() {
		return tax;
	}
	
	public void setTax(float tax) {
		this.tax = tax;
	}
		
	public float getDiscount() {
		return discount;
	}

	public void setDiscount(float discount) {
		this.discount = discount;
	}
		
}
