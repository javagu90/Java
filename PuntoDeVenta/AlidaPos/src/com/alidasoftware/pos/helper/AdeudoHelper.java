package com.alidasoftware.pos.helper;

import java.io.Serializable;
import java.util.Date;

import com.alidasoftware.pos.model.Formapago;

public class AdeudoHelper implements Serializable {

	private static final long serialVersionUID = 6566971943995699611L;
	
	private int idadeudo;

	private float cantidad;
	
	private String comentarios;
	
	private Date fecha;

	private String referenciapago;
	
	private Formapago formapago;
	
	private float cambio;
	
	public float getCambio() {
		return cambio;
	}

	public void setCambio(float cambio) {
		this.cambio = cambio;
	}

	public int getIdadeudo() {
		return idadeudo;
	}

	public void setIdadeudo(int idadeudo) {
		this.idadeudo = idadeudo;
	}


	public float getCantidad() {
		return cantidad;
	}

	public void setCantidad(float cantidad) {
		this.cantidad = cantidad;
	}

	public String getComentarios() {
		return comentarios;
	}

	public void setComentarios(String comentarios) {
		this.comentarios = comentarios;
	}

	public Date getFecha() {
		return fecha;
	}

	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}

	public String getReferenciapago() {
		return referenciapago;
	}

	public void setReferenciapago(String referenciapago) {
		this.referenciapago = referenciapago;
	}

	public Formapago getFormapago() {
		return formapago;
	}

	public void setFormapago(Formapago formapago) {
		this.formapago = formapago;
	}
}
